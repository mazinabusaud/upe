Graduate Requirements
A graduate student is defined as a person pursuing a graduate degree in Computer Science or Computer Engineering. Applicants

Must have completed at least 9 units of 600-Level CSCI/CINS/EECE course work applying toward the master’s degree requirement in Computer Science or Computer Engineering.
Must have a GPA of 3.50 or above in all course work applying toward the master’s degree, including all necessary prerequisites.
Must be committed to student community service while attending California State University, Chico.
Community Service
As a member, your service to the College of ECC student community includes 60 hours of any combination of the following:

Participation in UPE membership meetings.
Providing tutoring to fellow students in your major.
Being active in ongoing UPE organizational projects.
Supporting company recruitment visits to California State University, Chico.

If you meet these criteria, please fill out an application and submit it to the Computer Science Department Office (2nd floor, O'Connell Technology Center) by the posted deadline, usually November for Fall applicants and April for Spring.

If your application is accepted, you will need to pay the one-time, lifetime membership fee of $90 and attend the Initiation Ceremony in order to become an official member.

All information submitted will be verified against your transcripts. You may submit supplemental materials (resume, letter of recommendation) if you wish. If you have any questions regarding the membership requirements or any special circumstances that you wish considered, please contact us by sending electronic mail to the current president or by leaving a message for us in our mail box at the Computer Science Department office.


Provisional Membership
In the Fall semester of 1997, the CSUC Chapter of UPE enacted a Provisional Membership. This allows students to become involved with UPE events before they have satisfied the Membership Requirements listed above.

Students who are currently enrolled in the courses that will complete their eligibility may apply for Provisional Membership by completing the Membership Application. Provisional Members may participate in all UPE activities excluding:

Voting rights.
Holding office.
Applying for UPE scholarships.
Adding their signature to the Official Membership Roster.
At the end of the semester, Provisional Members will report to the UPE officers their grades for the required classes they were lacking for Permanent Membership. If the Provisional Member still satisfies the Membership Requirements, they will be invited to join as a Permanent Member with no additional cost. The one-time $90 fee must be collected at the time of Provisional Membership, and cannot be refunded should the Provisional Member fall below the Permanent Membership requirements.

